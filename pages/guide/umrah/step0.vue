  Umrah is a minor pilgrimage that can be performed at any time.

  <br /><br />

  Allah mentions in the Quran:

  <br /><br />

  "...accomplish Hajj and Umrah to please Allah". (Al-Baqrah 2, 196)

  <br /><br />

  Prophet Muhammad صلى الله عليه وسلم said:

  <br /><br />

  "One Umrah becomes an atonement for the sins committed up to the next Umrah"
  (Bukhari)

  <br /><br />

  Overview - Umrah has 4 main actions.

  <br />

  <ol>

    <li>

      Intention

    </li>

    <li>

      Tawaf (circumambulate around the Ka'bah)

    </li>

    <li>

      Sa'ey (walk between Mount Safa & Marwa)

    </li>

    <li>

      cut/trim hair

    </li>

  </ol>

  It is recommended to:

  <ul>

    <li>

      trim the nails, moustache, shave underarm & private parts

    </li>

    <li>

      take a shower

    </li>

    <li>

      perfume the body & not the ihram garments (men only) before making the
      intention for Umrah

    </li>

  </ul>

  Prohibitions after coming in the State of Ihram:

  <ul>

    <li>

      Using perfume

    </li>

    <li>

      Anointing the head or beard with oil, grease, or melted honey wax

      <br />

      (Difference of opinion as some allow)

    </li>

    <li>

      Removing or cutting finger nails, toe nails or hair

    </li>

    <li>

      Covering the face & wearing gloves for women with fitted garment

    </li>

    <li>

      Covering the head (for the man)

    </li>

    <li>

      For a man to wear clothes that fit around a limb

    </li>

    <li>

      Any marriage proposal, touching spouse with intimate feeling, sexual,
      hunting an edible, wild, land animal

    </li>

  </ul>

